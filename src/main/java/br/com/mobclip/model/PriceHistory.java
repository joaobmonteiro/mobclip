package br.com.mobclip.model;

import java.util.Date;
import org.springframework.data.annotation.Id;

public class PriceHistory {
	@Id
	private String id;
	private Date date;
	private Double price;
	
	public PriceHistory() {}
	
	public PriceHistory(Property existente) {
		date = new Date();
		price = existente.getPrice();
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public Double getPrice() {
		return price;
	}
	public void setPrice(Double price) {
		this.price = price;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (!(obj instanceof PriceHistory))
			return false;
		PriceHistory other = (PriceHistory) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}
}
